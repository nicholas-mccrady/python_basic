# you can assign values to more that one variable at a time using just a single line
# This can help shorten your programs and make the easier to read
# you will most often use this technique when initializing a set of numbers

# example
x,y,z = 1,2,3
print(x,y,z)

a,b,c = 'a','b','c'
print(a,b,c)

# python will initialize each value to its respectively positioned variable
# as long as the number of values and variables match them up correctly